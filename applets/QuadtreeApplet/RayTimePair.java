/**
 * Pair of ray times.
 */
public class RayTimePair {
	
	public double first, second;
	
	
	public RayTimePair() {
		
	}
	
	
	public RayTimePair(double first,
	                   double second) {
		
		this.first = first;
		this.second = second;
	}
}

